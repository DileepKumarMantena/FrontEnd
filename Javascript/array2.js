// Creating a 2D array
const matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

// Accessing elements in a 2D array
console.log(matrix[0][1]); // Output: 2

// Modifying elements in a 2D array
matrix[1][1] = 10;
console.log(matrix); // Output: [[1, 2, 3], [4, 10, 6], [7, 8, 9]]

console.log(matrix[0][2])