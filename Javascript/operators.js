// Arithmetic Operators:

   let a = 5;
   let b = 2;

   console.log(a + b); 
   console.log(a - b); 
   console.log(a * b); 
   console.log(a / b); 
   console.log(a % b); 
   console.log(a ** b);

   //Comparison Operators:

   let c = 5;

   console.log(c === 10); 
   console.log(c !== 10);
   console.log(c > 10);  
   console.log(c < 10);     
   console.log(c >= 10);
   console.log(c <= 10);
   
//**Logical Operators:

    let x = 6;
    let y = 3;

   console.log(x < 10 && y > 1);
   console.log(x < 10 || y < 1);
   console.log(x != y);

   
//Assignment Operators:
 
   let z = 10;

   z += 5;
   console.log(z);

   z -= 3;
   console.log(z);

   z *= 2;
   console.log(z);
  

